DynLayout ComputeDynLayout(double w, double h, int activeTab, bool compact)
{
  DynLayout L;
  const double pad = dynui::kPanelPad;
  L.header = { 0, 0, w, (double)dynui::kHeaderH };
  L.footer = { 0, h - (double)dynui::kFooterH, w, (double)dynui::kFooterH };

  const double hMid = L.header.h * 0.5;
  L.closeBtn = { w - pad - 18.0, hMid - 9.0, 18.0, 18.0 };
  L.abBtn    = { L.closeBtn.x - 48.0, hMid - 11.0, 40.0, 22.0 };
  // Preset box narrowed 120 -> 96 (v2.3.0) to make room for the mode button;
  // the preset name is CLIPPED to the box when it renders (long names ellipse
  // visually instead of spilling into the neighbours).
  L.preset   = { pad + 96.0, hMid - 11.0, 96.0, 22.0 };
  // DOWN/UP processor-mode STATE BUTTON (v2.3.0): header chrome right of the
  // preset box - it switches the whole gain computer, not a tab-local control.
  // A/B-style (shows the current mode, click flips). Header width budget at
  // kPanelW 480 is exact: the BOOST readout's caption is skipped by DrawHeader
  // if it would still touch this button (worst-case '+24.0' at kFsGrHero).
  L.modeBtn = { L.preset.x + L.preset.w + 8.0, hMid - 11.0, 48.0, 22.0 };

  const double bodyTop = L.header.h;
  const double bodyH = L.footer.y - bodyTop;

  if (compact) {
    // Compact mode: the hero plot + GR meter are hidden (the on-waveform overlay is
    // the visual); the knobs reflow into a wider 4-col x 2-row grid filling the body.
    L.plotWell = { 0, 0, 0, 0 };
    L.grMeter  = { 0, 0, 0, 0 };
    const int COLS = 4, ROWS = 2;
    const double gridX = pad, gridR = w - pad;
    const double colGap = (double)dynui::kKnobColGap;
    const double cCellW = (gridR - gridX - colGap * (COLS - 1)) / (double)COLS;
    const double gridTop = bodyTop + 8.0;
    const double cCellH = ((L.footer.y - 8.0) - gridTop) / (double)ROWS;
    auto ccell = [&](int ccol, int crow) -> URect {
      return { gridX + (double)ccol * (cCellW + colGap), gridTop + (double)crow * cCellH,
               cCellW, cCellH };
    };
    for (int i = 0; i < kDynNumParams; ++i) {
      const KnobSlot& s = COMPACT_KNOB_SLOTS[i];
      L.knob[i] = (s.tab == activeTab) ? ccell(s.col, s.row) : URect{ 0, 0, 0, 0 };
    }
    // Peak/RMS moved to the footer gap (v2.3.0) - M.Boost owns the ex-RMS cell.
    if (activeTab == 2) {   // View: DYN/ENV/GR/LIVE on row0; Compact on row1
      const double tH = 36.0;
      auto pill = [&](int ccol, int crow) -> URect {
        const URect c = ccell(ccol, crow); return { c.x, c.y + (c.h - tH) * 0.5, c.w, tH };
      };
      L.viewToggle[0] = pill(0, 0);  // DYN
      L.viewToggle[1] = pill(1, 0);  // ENV
      L.viewToggle[2] = pill(2, 0);  // GR
      L.viewToggle[3] = pill(3, 0);  // LIVE
      L.compactToggle = pill(0, 1);  // COMPACT
      // No A/B (it lives in the header) and no meter-scale (the plot + GR meter it
      // scales are hidden in compact, so the selector would do nothing).
    }
  } else {
  // Hero transfer plot: square on the LEFT, sized to the body height. The GR meter
  // is a bar-only column right of it (the hero number lives in the header for this
  // compact grid). The remaining width on the right holds the 2-col knob grid.
  const double plotSide = std::max(40.0, std::min(bodyH - 2.0 * pad, (w - 2.0 * pad) * 0.40));
  const double bodyMid = bodyTop + bodyH * 0.5;
  L.plotWell = { pad, bodyMid - plotSide * 0.5, plotSide, plotSide };
  L.grMeter  = { L.plotWell.x + plotSide + (double)dynui::kMeterGap, L.plotWell.y,
                 (double)dynui::kMeterBarW, plotSide };

  // Knob grid: kKnobCols x kKnobRows cells filling the band right of the meter,
  // vertically aligned with the plot. Each param maps to a (tab,col,row) slot;
  // off-tab params get an empty rect so render + hit-test both skip them.
  const double gridX = L.grMeter.x + L.grMeter.w + (double)dynui::kKnobGridGap;
  const double gridR = w - pad;
  const double colGap = (double)dynui::kKnobColGap;
  const double cellW = (gridR - gridX - colGap * (dynui::kKnobCols - 1)) / (double)dynui::kKnobCols;
  const double cellH = plotSide / (double)dynui::kKnobRows;
  auto cellRect = [&](int ccol, int crow) -> URect {
    return { gridX + (double)ccol * (cellW + colGap), L.plotWell.y + (double)crow * cellH,
             cellW, cellH };
  };
  for (int i = 0; i < kDynNumParams; ++i) {
    const KnobSlot& s = KNOB_SLOTS[i];
    L.knob[i] = (s.tab == activeTab) ? cellRect(s.col, s.row) : URect{ 0, 0, 0, 0 };
  }

  // Peak/RMS moved to the footer gap (v2.3.0) - M.Boost owns the ex-RMS cell.
  // View-tab state toggles fill the knob-grid cells: col0 rows 0-2 = Dyn/Env/GR
  // (overlay visibility), col1 row0 = Live, col1 row1 = Compact. (A/B is NOT here -
  // it lives in the header.) The meter-scale selector spans the bottom row.
  if (activeTab == 2) {
    static const int slot[4][2] = { {0,0}, {0,1}, {0,2}, {1,0} };  // DYN/ENV/GR/LIVE
    const double tH = 36.0;
    for (int i = 0; i < 4; ++i) {
      const URect c = cellRect(slot[i][0], slot[i][1]);
      L.viewToggle[i] = { c.x, c.y + (c.h - tH) * 0.5, c.w, tH };
    }
    // Compact-mode toggle: col1 row1 (below Live).
    const URect cc = cellRect(1, 1);
    L.compactToggle = { cc.x, cc.y + (cc.h - tH) * 0.5, cc.w, tH };
    // Meter-scale selector: a 4-segment dB-floor control on the bottom grid row,
    // spanning both columns. A "SCALE" caption (render-only) sits in the left gutter.
    const URect c0 = cellRect(0, 3), c1 = cellRect(1, 3);
    const double segH = 24.0, segGap = 2.0, capW = 44.0;
    const double segY = c0.y + (c0.h - segH) * 0.5;
    const double x0 = c0.x + capW, right = c1.x + c1.w;
    const int nSeg = dynui::kMeterFloorOptCount;
    const double segW = (right - x0 - (double)(nSeg - 1) * segGap) / (double)nSeg;
    for (int i = 0; i < nSeg; ++i)
      L.meterScale[i] = { x0 + (double)i * (segW + segGap), segY, segW, segH };
  }
  }

  const double fMid = L.footer.y + L.footer.h * 0.5;
  L.apply = { pad, fMid - 12.0, 84.0, 24.0 };
  const double segW = 86.0, segGap = 2.0;
  const double pillW = 3.0 * segW + 2.0 * segGap;
  const double pillX = w - pad - pillW;
  for (int i = 0; i < 3; ++i)
    L.tabSeg[i] = { pillX + (double)i * (segW + segGap), fMid - 12.0, segW, 24.0 };
  // Stage-power dots (v2.3.0 INC-4): a small bypass toggle INSIDE the COMP and
  // GATE pills (left edge; the rest of the pill still switches tabs) - visible
  // in every layout mode, tied visually to its stage.
  for (int i = 0; i < 2; ++i)
    L.stagePower[i] = { L.tabSeg[i].x + 4.0, L.tabSeg[i].y + 4.0, 16.0, 16.0 };
  // Peak/RMS detection pill (v2.3.0: moved here from the knob grid so M.Boost
  // could take that cell): centred in the footer gap between Apply and the tab
  // pill. Compressor tab only - detection mode pairs with the comp controls.
  if (activeTab == 0) {
    const double rmsSegW = 44.0, rmsH = 24.0;
    const double gapL = L.apply.x + L.apply.w, gapR = pillX;
    const double rx = gapL + ((gapR - gapL) - 2.0 * rmsSegW - 2.0) * 0.5;
    L.rms[0] = { rx,                  fMid - rmsH * 0.5, rmsSegW, rmsH };
    L.rms[1] = { rx + rmsSegW + 2.0,  fMid - rmsH * 0.5, rmsSegW, rmsH };
  }

  // Free-resize grip: bottom-right corner. Sits in the right pad margin, just past
  // the tab pill's right edge (w - pad), so it never overlaps the VIEW segment.
  L.resizeGrip = { w - 16.0, h - 16.0, 16.0, 16.0 };
  return L;
}

void ComputeCurveHandles(const URect& pw, const DynCurveParams& p, int activeTab, URect out[2])
{
  out[0] = URect{ 0, 0, 0, 0 };
  out[1] = URect{ 0, 0, 0, 0 };
  if (pw.w < 1.0 || pw.h < 1.0) return;   // no plot well (Compact mode) -> no handles
  const double span = (p.inMaxDb - p.inMinDb) != 0.0 ? (p.inMaxDb - p.inMinDb) : 1.0;
  auto dbToX = [&](double db) { return pw.x + (db - p.inMinDb) / span * pw.w; };
  auto dbToY = [&](double db) { return pw.y + pw.h - (db - p.inMinDb) / span * pw.h; };
  const double R = 13.0;                                  // hit radius (base px)
  const double inset = 6.0;                               // keep the ~5.5px disc inside the well
  auto box = [&](double db) -> URect {
    double cx = std::clamp(dbToX(db), pw.x + inset, pw.x + pw.w - inset);
    double cy = std::clamp(dbToY(CurveOutDb(db, p)), pw.y + inset, pw.y + pw.h - inset);
    return URect{ cx - R, cy - R, 2.0 * R, 2.0 * R };
  };
  if (activeTab == 0)                    out[0] = box(p.thresholdDb);   // knee handle (always)
  else if (activeTab == 1 && p.showGate) out[1] = box(p.gateThreshDb);  // gate node only when gate on
}

