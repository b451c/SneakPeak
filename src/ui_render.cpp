static inline BLRgba32 col(uint32_t argb) { return BLRgba32(argb); }
static inline BLRgba32 colA(uint32_t argb, int a) {
  return BLRgba32((uint32_t(a) << 24) | (argb & 0x00FFFFFFu));
}

// Load a BLFontFace from embedded data; returns an empty face on failure (call
// sites guard via create_from_face == BL_SUCCESS, so text degrades gracefully).
static BLFontFace LoadFace(const unsigned char* data, unsigned int size) {
  BLFontData fd;
  if (fd.create_from_data(data, size) != BL_SUCCESS) return BLFontFace();
  BLFontFace f;
  if (f.create_from_data(fd, 0) != BL_SUCCESS) return BLFontFace();
  return f;
}

// Cached faces (created once, live for the process). Medium for values/hero,
// Regular for labels/units/ticks (per design spec weight contrast).
static const BLFontFace& InterMediumFace() {
  static BLFontFace face = LoadFace(g_fontInterMedium, g_fontInterMedium_size);
  return face;
}
static const BLFontFace& InterRegularFace() {
  static BLFontFace face = LoadFace(g_fontInterRegular, g_fontInterRegular_size);
  return face;
}

// Advance width of a string in a font (to lay out adjacent text, e.g. a unit).
static double TextWidth(const BLFont& font, const char* s) {
  BLGlyphBuffer gb;
  gb.set_utf8_text(s);
  BLTextMetrics tm;
  if (font.get_text_metrics(gb, tm) != BL_SUCCESS) return 0.0;
  return tm.advance.x;
}

// Static-curve slope factor, MIRRORS DynSlopeFromRatio in dynamics_engine.h
// (kept local: this renderer carries no engine types beyond DynCurveParams).
// r >= 1 classic, r == 0 -> Inf:1 (S = -1), r < 0 -> over-compression (S < -1).
static double SlopeFromRatioUi(double r) {
  if (r == 0.0) return -1.0;
  if (r < 0.0) return 1.0 / r - 1.0;
  return 1.0 / (r < 1.0 ? 1.0 : r) - 1.0;
}

// Compression gain change (dB) for an input level, matching dynamics_engine.cpp
// ComputeCompression(): soft-knee gain computer, downward (reduce above thresh)
// or upward (boost below thresh, mirrored knee, min(GR, maxBoost) cap, boost
// floored at the gate threshold on the RAW input level - the gate always wins).
static double CompressionGrDb(double inDb, const DynCurveParams& p) {
  const double slope = SlopeFromRatioUi(p.ratio);
  const double halfKnee = p.kneeDb * 0.5;
  const double overshoot = inDb - p.thresholdDb;
  const bool hasDown = (p.mode != 1), hasUp = (p.mode != 0);
  // BOTH: knee centers shift +-halfKnee (dead-band leveler; same-center knees
  // cancel exactly) - mirrors dynamics_engine.cpp ComputeCompression.
  const double oDn = (p.mode == 2) ? overshoot - halfKnee : overshoot;
  const double oUp = (p.mode == 2) ? overshoot + halfKnee : overshoot;
  double g = 0.0;
  if (p.compBypass) return 0.0;   // stage bypassed: no comp contribution at all
  if (hasDown) {
    if (p.kneeDb > 0.0 && oDn > -halfKnee && oDn < halfKnee) {
      const double t = oDn + halfKnee;
      g += slope * t * t / (2.0 * p.kneeDb);
    } else if (oDn > 0.0) {
      g += slope * oDn;
    }
  }
  if (hasUp) {
    double gUp = 0.0;
    if (p.kneeDb > 0.0 && oUp > -halfKnee && oUp < halfKnee) {
      const double t = oUp - halfKnee;                     // mirrored knee quadratic
      gUp = -slope * t * t / (2.0 * p.kneeDb);
    } else if (oUp < 0.0) {
      gUp = slope * oUp;                                   // boost below threshold
    }
    gUp = std::min(gUp, p.maxBoostDb);
    if (p.showGate && inDb < p.gateThreshDb) gUp = 0.0;    // gate-coupled boost floor
    g += gUp;
  }
  return g;
}

// Full output transfer at an input level: y = x + compGR + makeup + gateGR (mirrors
// dynamics_engine ComputeCompression :306). Shared by the plotted curve and the
// curve-handle positions so the handle always sits exactly on the drawn line.
static double CurveOutDb(double in, const DynCurveParams& p) {
  const double g = CompressionGrDb(in, p);
  double out = in + g + p.makeupDb;
  if (p.showGate) {
    // Gate detection domain mirrors the engine: Down = post-comp pre-makeup,
    // Up = RAW input (boosted noise must never hold the gate open).
    const double detect = (p.mode != 0) ? in : in + g;
    // Closed-state ("basic") expander curve: slope Rg-1 below the open
    // threshold, clamped to range. Hysteresis only affects the time behavior
    // (band shading), never this static curve.
    if (detect < p.gateThreshDb) {
      const double gateSlope = std::max(1.0, p.gateRatio) - 1.0;
      out += std::max(gateSlope * (detect - p.gateThreshDb), -std::fabs(p.gateRangeDb));
    }
  }
  return out;
}

// Stacked-stroke additive glow for a path (Blend2D has no blur primitive).
static void GlowStroke(BLContext& ctx, const BLPath& path, uint32_t baseArgb) {
  ctx.set_comp_op(BL_COMP_OP_PLUS);
  struct Pass { double w; int a; };
  const Pass passes[3] = { {7.0, 26}, {4.0, 48}, {2.0, 90} };
  for (const Pass& p : passes) {
    ctx.set_stroke_width(p.w);
    ctx.stroke_path(path, colA(baseArgb, p.a));
  }
  ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
}

// --- cached Blend2D objects (must-do #4: no per-paint image/font allocation) -

struct Gfx {
  BLImage img;
  int imgW = 0, imgH = 0;
  BLFont fTick, fLabel, fUnit, fGrHero, fTab, fValue;
  bool fontsReady = false;

  void ensureFonts() {
    if (fontsReady) return;
    fontsReady =
        fTick  .create_from_face(InterRegularFace(), (float)dynui::kFsTick)   == BL_SUCCESS &&
        fLabel .create_from_face(InterRegularFace(), (float)dynui::kFsLabel)  == BL_SUCCESS &&
        fUnit  .create_from_face(InterRegularFace(), (float)dynui::kFsUnit)   == BL_SUCCESS &&
        fGrHero.create_from_face(InterMediumFace(),  (float)dynui::kFsGrHero) == BL_SUCCESS &&
        fTab   .create_from_face(InterMediumFace(),  (float)dynui::kFsTab)    == BL_SUCCESS &&
        fValue .create_from_face(InterMediumFace(),  (float)dynui::kFsValue)  == BL_SUCCESS;
  }
  // (re)create the offscreen image only when the device pixel size changes.
  bool ensureImage(int devW, int devH) {
    if (imgW != devW || imgH != devH) {
      if (img.create(devW, devH, BL_FORMAT_PRGB32) != BL_SUCCESS) return false;
      imgW = devW; imgH = devH;
    }
    return true;
  }
};

