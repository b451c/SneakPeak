static inline double Deg2Rad(double d) { return d * 3.14159265358979323846 / 180.0; }

// One knob in its cell: arc track (270deg) + value fill arc + center cap +
// indicator line + default-value tick on the ring, with the UPPERCASE label and
// value/unit text in a column to the right. Gate knobs fill violet, others amber.
static void DrawKnob(BLContext& ctx, const Gfx& gfx, const URect& cell, const KnobVM& k)
{
  if (cell.w < 1.0) return;   // not on the active tab
  const double dia = (double)dynui::kKnobDiaSm;
  const double r   = dia * 0.5;
  const double cx  = cell.x + 2.0 + r;
  const double cy  = cell.y + cell.h * 0.5;
  const double a0  = Deg2Rad(dynui::kKnobArcStartDeg);
  const double sw  = Deg2Rad(dynui::kKnobArcSweepDeg);
  const double norm = std::clamp(k.norm, 0.0, 1.0);
  const uint32_t fillCol = k.isGate ? dynui::kGateViolet : dynui::kAmber;

  // hover/drag: soft additive glow around the ring (the track + fill draw on top,
  // crisp, with the glow bleeding outside) - the "adjustable" affordance cue.
  if (k.hover) {
    ctx.set_comp_op(BL_COMP_OP_PLUS);
    BLPath ring; ring.arc_to(cx, cy, r, r, a0, sw, true);
    ctx.set_stroke_width(7.0); ctx.stroke_path(ring, colA(fillCol, 22));
    ctx.set_stroke_width(4.0); ctx.stroke_path(ring, colA(fillCol, 32));
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
  }

  // ring track (full sweep)
  ctx.set_stroke_width(3.0);
  { BLPath p; p.arc_to(cx, cy, r, r, a0, sw, true); ctx.stroke_path(p, col(dynui::kSurface2)); }
  // value fill (start -> norm)
  if (norm > 0.001) {
    BLPath p; p.arc_to(cx, cy, r, r, a0, sw * norm, true);
    ctx.stroke_path(p, col(fillCol));
  }
  // center cap (faint depth)
  ctx.fill_circle(BLCircle(cx, cy, r - 4.5), col(dynui::kSurface3));
  // indicator line at the current angle
  {
    const double a = a0 + sw * norm;
    ctx.set_stroke_width(2.0);
    ctx.stroke_line(BLLine(cx + std::cos(a) * (r - 12.0), cy + std::sin(a) * (r - 12.0),
                           cx + std::cos(a) * (r - 3.0),  cy + std::sin(a) * (r - 3.0)),
                    col(fillCol));
  }
  // default-value tick notch just outside the ring
  {
    const double a = a0 + sw * std::clamp(k.defaultNorm, 0.0, 1.0);
    const double r0 = r + 1.0, r1 = r + 1.0 + (double)dynui::kDefaultTickLen;
    ctx.set_stroke_width(1.0);
    ctx.stroke_line(BLLine(cx + std::cos(a) * r0, cy + std::sin(a) * r0,
                           cx + std::cos(a) * r1, cy + std::sin(a) * r1),
                    colA(dynui::kInkMuted, 200));
  }
  // label + value text column to the right of the knob
  if (gfx.fontsReady) {
    const double tx = cell.x + dia + (double)dynui::kKnobTextGap;
    char up[24];
    int n = 0;
    for (const char* s = k.label ? k.label : ""; *s && n < (int)sizeof(up) - 1; ++s)
      up[n++] = (char)std::toupper((unsigned char)*s);
    up[n] = '\0';
    ctx.fill_utf8_text(BLPoint(tx, cy - 3.0), gfx.fLabel, up, SIZE_MAX, col(dynui::kInkSecondary));

    const double vy = cy + 15.0;
    if (k.editing) {
      // Inline type-value editor (Inc 8): a surface3 box with an amber outline holds
      // the live text + a solid amber caret, replacing the readout. The knob arc still
      // shows the OLD value so the change is visible until committed. The box spans the
      // readout column (tx -> cell right) so typical entries ("1000", "-18.5") fit.
      const char* et = k.editText ? k.editText : "";
      const double boxX = std::max(cell.x, tx - 3.0);          // never spill left of the cell
      const double boxW = std::max(0.0, cell.x + cell.w - boxX); // ...nor past its right edge
      const double boxY = vy - 13.0, boxH = 18.0;
      ctx.fill_round_rect(BLRoundRect(boxX, boxY, boxW, boxH, dynui::kRadiusCtrl),
                          col(dynui::kSurface3));
      ctx.set_stroke_width(1.0);
      ctx.stroke_round_rect(BLRoundRect(boxX + 0.5, boxY + 0.5, boxW - 1.0, boxH - 1.0,
                                        dynui::kRadiusCtrl), col(dynui::kAmber));
      // Clip text + caret to the box so an over-long entry can't bleed past the edge.
      ctx.save();
      ctx.clip_to_rect(BLRect(boxX, boxY, boxW, boxH));
      ctx.fill_utf8_text(BLPoint(tx, vy), gfx.fValue, et, SIZE_MAX, col(dynui::kInkPrimary));
      if (k.caretOn) {   // blink (motion pass): solid right after a keystroke, then ~1s on/off
        const double caretX = tx + TextWidth(gfx.fValue, et) + 1.0;
        ctx.fill_rect(BLRect(caretX, vy - 11.0, 1.6, 14.0), col(dynui::kAmber));
      }
      ctx.restore();
    } else {
      // Value number (16px primary) + unit (11px muted) on its own advance, per the
      // type spec - keeps long readouts ("1000 ms") inside the content margin and lets
      // ":1" abut the number. Auto-makeup shows the computed value with a muted "auto".
      // showOff (G.Thr at the sentinel / hard-left detent) renders "Off", no unit;
      // showInf (Ratio at the Inf:1 sentinel) renders "Inf:1".
      char num[24];
      if (k.showOff) std::snprintf(num, sizeof(num), "Off");
      else if (k.showInf) std::snprintf(num, sizeof(num), "Inf");
      else std::snprintf(num, sizeof(num), k.precision == 1 ? "%.1f" : "%.0f", k.value);
      ctx.fill_utf8_text(BLPoint(tx, vy), gfx.fValue, num, SIZE_MAX, col(dynui::kInkPrimary));
      const char* unit = k.showOff ? "" : (k.showAuto ? "auto" : (k.unit ? k.unit : ""));
      if (*unit) {
        const bool abut = (unit[0] == ':');   // ratio ":1" sits flush against the number
        const double ux = tx + TextWidth(gfx.fValue, num) + (abut ? 1.0 : 4.0);
        ctx.fill_utf8_text(BLPoint(ux, vy), gfx.fUnit, unit, SIZE_MAX, col(dynui::kInkMuted));
      }
    }
  }
}

// Ensure the platform surface + cached BLImage/fonts are ready at devW x devH.
bool UiCanvas::prepareSurface(HDC hdc, int devW, int devH)
{
  if (!ensure(hdc, devW, devH)) return false;
  if (!m_gfx) m_gfx = std::make_unique<Gfx>();
  m_gfx->ensureFonts();
  return m_gfx->ensureImage(devW, devH);
}

// Copy the rendered BLImage scanlines into the platform framebuffer (forcing
// opaque alpha), then scaled-blit to the destination DC. dpr==1 -> devW==w -> 1:1
// (Windows/Linux unchanged); dpr>1 onto the real Retina window DC -> SWELL maps
// the 2x source onto the 2x backing => crisp HiDPI. (Plain BitBlt(devW,devH) would
// place device-sized px at a logical position = 2x-too-large; the BitBlt-1:1 trap
// documented in .harness/design_phase2_architecture.md.)
void UiCanvas::presentSurface(HDC hdc, int x, int y, int w, int h, int devW, int devH)
{
  unsigned int* fbuf = nullptr;
#ifdef _WIN32
  BITMAP bm;
  if (GetObject(m_memBmp, sizeof(bm), &bm)) fbuf = (unsigned int*)bm.bmBits;
#else
  fbuf = (unsigned int*)SWELL_GetCtxFrameBuffer(m_memDC);
#endif
  if (!fbuf) return;

  BLImageData dat;
  if (m_gfx->img.get_data(&dat) != BL_SUCCESS) return;
  if (!dat.pixel_data) return;
  if (dat.stride <= 0 || (dat.stride % 4) != 0 || dat.stride < (intptr_t)devW * 4) return;
  const uint8_t* srcBase = (const uint8_t*)dat.pixel_data;
  // Dest rows advance by the REAL surface stride (m_strideW, alignment-padded in
  // ensure()) - never by devW, which shears on the Linux LICE backend (see ensure).
  for (int row = 0; row < devH; ++row) {
    const unsigned int* s = (const unsigned int*)(srcBase + (intptr_t)row * dat.stride);
    unsigned int* d = fbuf + (size_t)row * (size_t)m_strideW;
    for (int col2 = 0; col2 < devW; ++col2)
      d[col2] = s[col2] | 0xFF000000u;
  }
  StretchBlt(hdc, x, y, w, h, m_memDC, 0, 0, devW, devH, SRCCOPY);
}

