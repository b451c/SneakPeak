// Base-coords geometry, shared with SettingsPanel hit-testing. Vertical rhythm:
// header 44, then per-section caption row + control rows with even spacing.
// kSettingsH (200) == the final y + 16 bottom pad - keep them in sync.
SettingsLayout ComputeSettingsLayout(double w, double h)
{
  (void)h;   // fixed vertical rhythm; h is the (matching) kSettingsH
  SettingsLayout L;
  const double pad = (double)dynui::kPanelPad;
  L.header   = { 0, 0, w, (double)dynui::kHeaderH };
  L.closeBtn = { w - pad - 18.0, L.header.h * 0.5 - 9.0, 18.0, 18.0 };

  double y = L.header.h + 14.0;
  L.scaleLabel = { pad, y, 120.0, 16.0 };
  L.scaleValue = { w - pad - 80.0, y, 80.0, 16.0 };
  y += 22.0;
  L.sliderRow   = { pad, y, w - 2.0 * pad, 28.0 };
  // Track inset by the thumb radius so the thumb never clips the panel edge.
  L.sliderTrack = { pad + 8.0, y + 12.0, w - 2.0 * pad - 16.0, 4.0 };
  y += 38.0;
  const double segW = (w - 2.0 * pad - 12.0) / 3.0;
  for (int i = 0; i < 3; ++i)
    L.density[i] = { pad + i * (segW + 6.0), y, segW, 28.0 };
  y += 38.0;
  L.fitBtn = { pad, y, w - 2.0 * pad, 28.0 };
  y += 28.0;

  // Migrated preference sections (A2b): divider (drawn at caption.y - 12), caption,
  // then the control row(s). Even rhythm: 12 above the divider, 12 to the caption,
  // 20 from caption top to its control row.
  y += 24.0;   // 12 gap + (divider) + 12
  L.rulerCaption = { pad, y, 160.0, 14.0 };
  y += 20.0;
  for (int i = 0; i < 3; ++i)
    L.rulerSeg[i] = { pad + i * (segW + 6.0), y, segW, 26.0 };
  y += 26.0;

  y += 24.0;
  L.metersCaption = { pad, y, 160.0, 14.0 };
  L.masterToggle  = { w - pad - 92.0, y - 4.0, 92.0, 22.0 };  // on the caption row, right
  y += 20.0;
  for (int i = 0; i < 3; ++i)
    L.meterSeg[i] = { pad + i * (segW + 6.0), y, segW, 26.0 };
  y += 26.0;

  y += 24.0;
  L.viewCaption = { pad, y, 160.0, 14.0 };
  y += 20.0;
  const double pillW = (w - 2.0 * pad - 8.0) / 2.0;
  L.viewToggle[0] = { pad, y, pillW, 26.0 };                  // METERS
  L.viewToggle[1] = { pad + pillW + 8.0, y, pillW, 26.0 };    // RMS
  y += 32.0;
  L.viewToggle[2] = { pad, y, pillW, 26.0 };                  // SNAP TO ZERO
  L.viewToggle[3] = { pad + pillW + 8.0, y, pillW, 26.0 };    // MINIMAP
  y += 32.0;
  L.zoomSeg[0] = { pad, y, pillW, 26.0 };                     // ZOOM: MOUSE
  L.zoomSeg[1] = { pad + pillW + 8.0, y, pillW, 26.0 };       // ZOOM: CURSOR
  // final y + 26 + 16 bottom pad == kSettingsH (474) - keep in sync with ui_theme.h
  return L;
}

void UiCanvas::RenderSettingsPanel(HDC hdc, int x, int y, int w, int h, double dpr,
                                   const SettingsVM& vm)
{
  if (!hdc || w < 8 || h < 8) return;
  if (dpr < 1.0) dpr = 1.0;
  // Crisp-blit invariant: device buffer from the exact blitted w/h; the context
  // scales base content to fill it exactly (see RenderPanel for the rationale).
  const int devW = (int)std::lround((double)w * dpr);
  const int devH = (int)std::lround((double)h * dpr);
  if (!prepareSurface(hdc, devW, devH)) return;
  {
    BLContext ctx;
    if (ctx.begin(m_gfx->img) != BL_SUCCESS) return;
    ctx.clear_all();
    ctx.scale((double)devW / (double)dynui::kSettingsW,
              (double)devH / (double)dynui::kSettingsH);
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    const Gfx& gfx = *m_gfx;
    const double W = (double)dynui::kSettingsW, H = (double)dynui::kSettingsH;

    // panel slab: same treatment as the dynamics panel (gradient + hairline)
    {
      BLGradient bg(BLLinearGradientValues(0, 0, 0, H));
      bg.add_stop(0.0, col(dynui::kSurface1));
      bg.add_stop(1.0, col(dynui::kSurface0));
      ctx.fill_round_rect(BLRoundRect(0, 0, W, H, dynui::kRadiusPanel), bg);
      ctx.set_stroke_width(1.0);
      ctx.stroke_round_rect(BLRoundRect(0.5, 0.5, W - 1, H - 1, dynui::kRadiusPanel),
                            col(dynui::kHairline));
    }

    const SettingsLayout L = ComputeSettingsLayout(W, H);

    // header: title + close
    if (gfx.fontsReady) {
      ctx.fill_utf8_text(BLPoint(dynui::kPanelPad, L.header.h * 0.5 + 5.0), gfx.fValue,
                         "SETTINGS", SIZE_MAX, col(dynui::kInkPrimary));
      TextCentered(ctx, gfx.fValue, L.closeBtn, "x",
                   vm.hover == SET_HIT_CLOSE ? dynui::kInkPrimary : dynui::kInkMuted);
    }
    ctx.set_stroke_width(1.0);
    ctx.stroke_line(BLLine(0, L.header.h, W, L.header.h), col(dynui::kHairline));

    // UI SCALE caption + live % readout (amber while dragging)
    if (gfx.fontsReady) {
      ctx.fill_utf8_text(BLPoint(L.scaleLabel.x, L.scaleLabel.y + 12.0), gfx.fLabel,
                         "UI SCALE", SIZE_MAX, col(dynui::kInkSecondary));
      char pct[16];
      std::snprintf(pct, sizeof(pct), "%d%%", (int)std::lround(vm.uiScale * 100.0));
      const double tw = TextWidth(gfx.fValue, pct);
      ctx.fill_utf8_text(BLPoint(L.scaleValue.x + L.scaleValue.w - tw, L.scaleValue.y + 13.0),
                         gfx.fValue, pct, SIZE_MAX,
                         col(vm.dragging ? dynui::kAmber : dynui::kInkPrimary));
    }

    // slider: rail + amber fill + 100% reference notch + thumb (glow on hover/drag)
    {
      const double norm = std::clamp((vm.uiScale - 0.8) / 1.2, 0.0, 1.0);
      const double cy = L.sliderTrack.y + L.sliderTrack.h * 0.5;
      const double tx = L.sliderTrack.x + norm * L.sliderTrack.w;
      FillURound(ctx, L.sliderTrack, 2.0, dynui::kSurface2);
      if (tx > L.sliderTrack.x + 0.5)
        ctx.fill_round_rect(BLRoundRect(L.sliderTrack.x, L.sliderTrack.y,
                                        tx - L.sliderTrack.x, L.sliderTrack.h, 2.0),
                            col(dynui::kAmber));
      {
        const double nx = L.sliderTrack.x + ((1.0 - 0.8) / 1.2) * L.sliderTrack.w;
        ctx.fill_rect(BLRect(nx - 0.75, cy - 8.0, 1.5, 4.5), colA(dynui::kInkMuted, 180));
      }
      const bool hot = vm.dragging || vm.hover == SET_HIT_SLIDER;
      if (hot) {
        ctx.set_comp_op(BL_COMP_OP_PLUS);
        ctx.fill_circle(BLCircle(tx, cy, 11.0), colA(dynui::kAmber, 26));
        ctx.fill_circle(BLCircle(tx, cy, 8.5), colA(dynui::kAmber, 40));
        ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
      }
      ctx.fill_circle(BLCircle(tx, cy, 7.0), col(dynui::kAmber));
      ctx.fill_circle(BLCircle(tx, cy, 2.6), col(dynui::kSurface0));
    }

    // density presets: 3-segment selector; the segment matching the current scale
    // lights up (none when the slider sits between presets)
    {
      static const char* const kDensityLabels[3] = { "COMPACT", "COMFORTABLE", "SPACIOUS" };
      int active = -1;
      for (int i = 0; i < 3; ++i)
        if (std::fabs(vm.uiScale - dynui::kDensityScale[i]) < 0.005) active = i;
      DrawSegmented3(ctx, gfx, L.density, kDensityLabels, active);
    }

    // fit-to-window
    FillURound(ctx, L.fitBtn, dynui::kRadiusCtrl,
               vm.hover == SET_HIT_FIT ? dynui::kSurface3 : dynui::kSurface2);
    if (gfx.fontsReady)
      TextCentered(ctx, gfx.fLabel, L.fitBtn, "FIT TO WINDOW",
                   vm.hover == SET_HIT_FIT ? dynui::kInkPrimary : dynui::kInkSecondary);

    // Migrated preference sections (A2b): divider + caption + controls each.
    auto sectionCaption = [&](const URect& cap, const char* text) {
      ctx.set_stroke_width(1.0);
      ctx.stroke_line(BLLine(dynui::kPanelPad, cap.y - 12.0,
                             W - dynui::kPanelPad, cap.y - 12.0), col(dynui::kHairline));
      if (gfx.fontsReady)
        ctx.fill_utf8_text(BLPoint(cap.x, cap.y + 11.0), gfx.fLabel, text, SIZE_MAX,
                           col(dynui::kInkSecondary));
    };

    // RULER: time display mode
    sectionCaption(L.rulerCaption, "RULER");
    {
      static const char* const kRulerLabels[3] = { "RELATIVE", "ABSOLUTE", "BARS/BEATS" };
      DrawSegmented3(ctx, gfx, L.rulerSeg, kRulerLabels, vm.prefs.rulerMode);
    }

    // METERS: detection mode + master-output source pill on the caption row
    sectionCaption(L.metersCaption, "METERS");
    DrawTogglePill(ctx, gfx, L.masterToggle, "MASTER", vm.prefs.meterFromMaster,
                   dynui::kAmber, false, 0.0);
    {
      static const char* const kMeterLabels[3] = { "PEAK", "RMS", "VU" };
      DrawSegmented3(ctx, gfx, L.meterSeg, kMeterLabels, vm.prefs.meterMode);
    }

    // VIEW: independent display toggles
    sectionCaption(L.viewCaption, "VIEW");
    DrawTogglePill(ctx, gfx, L.viewToggle[0], "METERS",       vm.prefs.showMeters, dynui::kAmber, false, 0.0);
    DrawTogglePill(ctx, gfx, L.viewToggle[1], "RMS",          vm.prefs.showRMS,    dynui::kAmber, false, 0.0);
    DrawTogglePill(ctx, gfx, L.viewToggle[2], "SNAP TO ZERO", vm.prefs.snapZero,   dynui::kAmber, false, 0.0);
    DrawTogglePill(ctx, gfx, L.viewToggle[3], "MINIMAP",      vm.prefs.minimap,    dynui::kAmber, false, 0.0);
    // Wheel-zoom center (#83): a 2-way selector, mouse position vs edit cursor.
    // "EDIT CURSOR" spelled out (REAPER's own term) - bare "CURSOR" reads as the
    // mouse pointer, the exact ambiguity this option exists to resolve.
    DrawSegmented2(ctx, gfx, L.zoomSeg[0], L.zoomSeg[1], "ZOOM: MOUSE", "ZOOM: EDIT CURSOR",
                   vm.prefs.zoomOnCursor);

    if (ctx.end() != BL_SUCCESS) return;
  }
  presentSurface(hdc, x, y, w, h, devW, devH);
}

// --- the premium gain knob overlay (v2.2.0 Inc D) ----------------------------

void UiCanvas::RenderGainPanel(HDC hdc, int x, int y, int w, int h, double dpr,
                               const GainVM& vm)
{
  if (!hdc || w < 8 || h < 8) return;
  if (dpr < 1.0) dpr = 1.0;
  // Base layout = the GDI panel's 110x32 literal coords (knob zone x<32, readout
  // 32..96, close 96..110/y<14), so GainPanel's SP()-scaled hit-test matches.
  // Crisp-blit invariant: device buffer from the exact blitted w/h (see RenderPanel).
  const double BW = 110.0, BH = 32.0;
  const int devW = (int)std::lround((double)w * dpr);
  const int devH = (int)std::lround((double)h * dpr);
  if (!prepareSurface(hdc, devW, devH)) return;
  {
    BLContext ctx;
    if (ctx.begin(m_gfx->img) != BL_SUCCESS) return;
    ctx.clear_all();
    ctx.scale((double)devW / BW, (double)devH / BH);
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    const Gfx& gfx = *m_gfx;

    // slab (small: control radius, not panel radius)
    {
      BLGradient bg(BLLinearGradientValues(0, 0, 0, BH));
      bg.add_stop(0.0, col(dynui::kSurface1));
      bg.add_stop(1.0, col(dynui::kSurface0));
      ctx.fill_round_rect(BLRoundRect(0, 0, BW, BH, dynui::kRadiusCtrl), bg);
      ctx.set_stroke_width(1.0);
      ctx.stroke_round_rect(BLRoundRect(0.5, 0.5, BW - 1, BH - 1, dynui::kRadiusCtrl),
                            col(dynui::kHairline));
    }

    // knob: same 270deg geometry as the dynamics knobs (screen 135deg + sweep 270)
    const double cx = 18.0, cy = BH * 0.5, r = 10.0;
    const double a0 = Deg2Rad(135.0), sweep = Deg2Rad(270.0);
    const uint32_t accent = vm.boost ? dynui::kGrRed : dynui::kAmber;
    ctx.set_stroke_width(2.5);
    { BLPath p; p.arc_to(cx, cy, r, r, a0, sweep, true); ctx.stroke_path(p, col(dynui::kSurface3)); }
    // active arc: 0 dB notch -> current value
    {
      const double nLo = std::min(vm.zeroNorm, vm.valNorm);
      const double nHi = std::max(vm.zeroNorm, vm.valNorm);
      if (nHi - nLo > 0.002) {
        BLPath p;
        p.arc_to(cx, cy, r, r, a0 + sweep * nLo, sweep * (nHi - nLo), true);
        ctx.stroke_path(p, col(accent));
      }
    }
    // cap + pointer
    ctx.fill_circle(BLCircle(cx, cy, r - 3.5), col(dynui::kSurface2));
    {
      const double a = a0 + sweep * std::clamp(vm.valNorm, 0.0, 1.0);
      ctx.set_stroke_width(2.0);
      ctx.stroke_line(BLLine(cx + std::cos(a) * 2.5, cy + std::sin(a) * 2.5,
                             cx + std::cos(a) * (r - 1.5), cy + std::sin(a) * (r - 1.5)),
                      col(accent));
    }
    // 0 dB notch tick just outside the ring
    {
      const double a = a0 + sweep * std::clamp(vm.zeroNorm, 0.0, 1.0);
      ctx.set_stroke_width(1.0);
      ctx.stroke_line(BLLine(cx + std::cos(a) * (r + 1.0), cy + std::sin(a) * (r + 1.0),
                             cx + std::cos(a) * (r + 4.0), cy + std::sin(a) * (r + 4.0)),
                      colA(dynui::kInkMuted, 200));
    }

    // dB readout, centered in [32, 96] (gold tint = batch relative mode)
    if (gfx.fontsReady && vm.text) {
      const URect tr{ 32.0, 0.0, 64.0, BH };
      const uint32_t ink = vm.boost ? dynui::kGrRed
                         : vm.gold  ? dynui::kAmberGlow : dynui::kInkPrimary;
      TextCentered(ctx, gfx.fTab, tr, vm.text, ink);
    }
    // close x (top-right 14px zone)
    if (gfx.fontsReady) {
      const URect xr{ BW - 14.0, 0.0, 14.0, 16.0 };
      TextCentered(ctx, gfx.fLabel, xr, "x", dynui::kInkMuted);
    }

    if (ctx.end() != BL_SUCCESS) return;
  }
  presentSurface(hdc, x, y, w, h, devW, devH);
}

// --- the premium L/R meters (v2.2.0 Inc E) -----------------------------------

void UiCanvas::RenderMeters(HDC hdc, int x, int y, int w, int h, double dpr,
                            const MetersVM& vm)
{
  if (!hdc || w < 16 || h < 16) return;
  if (dpr < 1.0) dpr = 1.0;
  // The meters fill a layout rect (variable size), so the base coords are the
  // rect divided by the global UI scale - content (bars, fonts, spacing) then
  // scales with the UI like the fixed-base panels do.
  const double S = vm.uiScale > 0.0 ? vm.uiScale : 1.0;
  const double BW = (double)w / S, BH = (double)h / S;
  const int devW = (int)std::lround((double)w * dpr);
  const int devH = (int)std::lround((double)h * dpr);
  if (!prepareSurface(hdc, devW, devH)) return;
  {
    BLContext ctx;
    if (ctx.begin(m_gfx->img) != BL_SUCCESS) return;
    ctx.clear_all();
    ctx.scale((double)devW / BW, (double)devH / BH);
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    const Gfx& gfx = *m_gfx;

    // background slab (flat surface0 - sits inside the GDI bottom panel)
    ctx.fill_rect(BLRect(0, 0, BW, BH), col(dynui::kSurface0));

    // geometry: mirrors the GDI meter (labels left, bars right, scale row below)
    const double scaleH = 12.0, gap = 2.0, labelW = 14.0;
    const double barsH = BH - scaleH;
    const int numCh = vm.numCh >= 2 ? 2 : 1;
    const double barH = (numCh == 2) ? (barsH - gap) / 2.0 : barsH;
    const double barLeft = labelW, barRight = BW - 2.0;
    const double barW = barRight - barLeft;
    if (barW < 10.0) { ctx.end(); return; }

    // per-mode bar shades (Peak bright / RMS mid / VU dim - the GDI distinction)
    struct Shades { uint32_t g, yel, r; };
    static const Shades kShades[3] = {
      { 0xFF32D25A, 0xFFD2BE28, 0xFFDC3232 },   // PEAK
      { 0xFF28A03C, 0xFFBEAA1E, 0xFFC82828 },   // RMS
      { 0xFF1E9646, 0xFFB4A01E, 0xFFBE2828 },   // VU
    };
    const Shades& sh = kShades[vm.mode >= 0 && vm.mode <= 2 ? vm.mode : 0];
    const double nYellow = (-18.0 + 60.0) / 60.0;   // zone boundaries (dB -> norm)
    const double nRed    = (-6.0 + 60.0) / 60.0;

    static const char* const chLabels2[2] = { "L", "R" };
    for (int ch = 0; ch < numCh; ++ch) {
      const double yTop = ch * (barH + gap);

      // channel label
      if (gfx.fontsReady)
        TextCentered(ctx, gfx.fTick, URect{ 0.0, yTop, labelW, barH },
                     numCh == 1 ? "M" : chLabels2[ch], dynui::kInkSecondary);

      // bar well
      ctx.fill_round_rect(BLRoundRect(barLeft, yTop, barW, barH, 2.0),
                          col(dynui::kSurface2));

      // value bar: green->yellow->red gradient, clipped to the current level
      const double bn = std::clamp(vm.barNorm[ch], 0.0, 1.0);
      if (bn > 0.003) {
        BLGradient g(BLLinearGradientValues(barLeft, 0, barLeft + barW, 0));
        g.add_stop(0.0,            col(sh.g));
        g.add_stop(nYellow - 0.02, col(sh.g));
        g.add_stop(nYellow + 0.02, col(sh.yel));
        g.add_stop(nRed - 0.02,    col(sh.yel));
        g.add_stop(nRed + 0.02,    col(sh.r));
        g.add_stop(1.0,            col(sh.r));
        ctx.save();
        ctx.clip_to_rect(BLRect(barLeft, yTop, barW * bn, barH));
        ctx.fill_round_rect(BLRoundRect(barLeft, yTop + 1.0, barW, barH - 2.0, 2.0), g);
        ctx.restore();
      }

      // dB ticks inside the well (subtle)
      static const double kTicksDb[] = { -48, -36, -24, -18, -12, -6, -3 };
      for (double db : kTicksDb) {
        const double tx = barLeft + ((db + 60.0) / 60.0) * barW;
        ctx.fill_rect(BLRect(tx, yTop + 1.0, 1.0, barH - 2.0), colA(0xFFFFFFFF, 14));
      }

      // peak-hold: 2px line colored by zone
      const double pn = std::clamp(vm.peakNorm[ch], 0.0, 1.0);
      if (pn > 0.01) {
        const uint32_t pc = pn > nRed ? sh.r : pn > nYellow ? sh.yel : sh.g;
        ctx.fill_rect(BLRect(barLeft + pn * barW - 1.0, yTop, 2.0, barH), col(pc));
      }
    }

    // scale row: mode caption + dB numbers under the bars
    if (gfx.fontsReady) {
      const double sy = BH - scaleH;
      TextCentered(ctx, gfx.fTick, URect{ 0.0, sy, labelW, scaleH },
                   vm.modeLabel ? vm.modeLabel : "dB", dynui::kInkMuted);
      static const double kMarksDb[] = { -54, -48, -42, -36, -30, -24, -18, -12, -6, -3, 0 };
      for (double db : kMarksDb) {
        const double mx = barLeft + ((db + 60.0) / 60.0) * barW;
        ctx.fill_rect(BLRect(mx, sy, 1.0, 3.0), colA(0xFFFFFFFF, 30));
        char buf[8];
        std::snprintf(buf, sizeof(buf), "%.0f", db);
        TextCentered(ctx, gfx.fTick, URect{ mx - 14.0, sy + 2.0, 28.0, scaleH - 2.0 },
                     buf, dynui::kInkMuted);
      }
    }

    if (ctx.end() != BL_SUCCESS) return;
  }
  presentSurface(hdc, x, y, w, h, devW, devH);
}

// --- the premium toast (v2.2.0 Inc F) ----------------------------------------

void UiCanvas::RenderToast(HDC hdc, int x, int y, int w, int h, double dpr,
                           const ToastVM& vm)
{
  if (!hdc || w < 8 || h < 8 || !vm.text || !vm.text[0]) return;
  if (dpr < 1.0) dpr = 1.0;
  const double S = vm.uiScale > 0.0 ? vm.uiScale : 1.0;
  const double BW = (double)w / S, BH = (double)h / S;
  const int devW = (int)std::lround((double)w * dpr);
  const int devH = (int)std::lround((double)h * dpr);
  if (!prepareSurface(hdc, devW, devH)) return;
  {
    BLContext ctx;
    if (ctx.begin(m_gfx->img) != BL_SUCCESS) return;
    ctx.clear_all();
    ctx.scale((double)devW / BW, (double)devH / BH);
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    const Gfx& gfx = *m_gfx;

    // Opaque underlay in the waveform bg color (the blit rect is opaque; this
    // makes it blend with what it covers), then the pill composites over it
    // with the fade alpha - a real alpha fade against the right background.
    ctx.fill_rect(BLRect(0, 0, BW, BH), col(vm.bgColor));
    ctx.set_global_alpha(std::clamp(vm.alpha, 0.0, 1.0));

    BLGradient bg(BLLinearGradientValues(0, 0, 0, BH));
    bg.add_stop(0.0, col(dynui::kSurface3));
    bg.add_stop(1.0, col(dynui::kSurface1));
    ctx.fill_round_rect(BLRoundRect(0, 0, BW, BH, dynui::kRadiusCtrl), bg);
    ctx.set_stroke_width(1.0);
    ctx.stroke_round_rect(BLRoundRect(0.5, 0.5, BW - 1, BH - 1, dynui::kRadiusCtrl),
                          col(dynui::kHairline));
    if (gfx.fontsReady)
      TextCentered(ctx, gfx.fTab, URect{ 0.0, 0.0, BW, BH }, vm.text,
                   dynui::kInkPrimary);

    if (ctx.end() != BL_SUCCESS) return;
  }
  presentSurface(hdc, x, y, w, h, devW, devH);
}
