// Transfer-curve plot inside the square well at (px0,py0,plotSide): well bg,
// unity diagonal, reduction wedge, dotted threshold, gate cliff, static curve,
// amber operating-point segment + glow dot, and input-axis dB tick labels.
// Drawn in logical coordinates; mirrors dynamics_engine ComputeCompression math.
static void DrawTransferPlot(BLContext& ctx, const Gfx& gfx,
                             double px0, double py0, double plotSide,
                             const DynCurveParams& p)
{
  const double px1 = px0 + plotSide;
  const double py1 = py0 + plotSide;
  ctx.fill_round_rect(BLRoundRect(px0, py0, plotSide, plotSide, dynui::kRadiusCtrl),
                      col(dynui::kSurface1));

  const double inMin = p.inMinDb, inMax = p.inMaxDb;
  const double span = (inMax - inMin) != 0 ? (inMax - inMin) : 1.0;
  auto dbToX = [&](double db) { return px0 + (db - inMin) / span * plotSide; };
  auto dbToY = [&](double db) { return py1 - (db - inMin) / span * plotSide; };

  // Curve math mirrors dynamics_engine ComputeCompression() steady state. The
  // plotted curve is the COMPRESSION CHARACTERISTIC y = x + GR (per the design
  // spec); makeup gain is a constant offset shown via its own control, NOT baked
  // into the curve (keeps red=reduction and the wedge clean). The GATE onset/depth
  // DO depend on makeup (the engine gates on the post-comp+makeup level), so
  // makeup is used only inside gateGrDb to place the cliff correctly.
  auto compGrDb = [&](double in) {
    return CompressionGrDb(in, p.thresholdDb, p.ratio, p.kneeDb);            // <= 0
  };
  auto gateGrDb = [&](double in, double compGr) -> double {
    if (!p.showGate) return 0.0;
    const double postLvl = in + compGr + p.makeupDb;                         // engine: post-comp+makeup
    if (postLvl >= p.gateThreshDb) return 0.0;
    const double gateRangeNeg = -std::fabs(p.gateRangeDb);                   // engine range is negative
    return std::max(postLvl - p.gateThreshDb, gateRangeNeg);                 // ramp 0 -> range, clamped
  };
  auto compOut = [&](double in) { return in + compGrDb(in); };              // wedge: compression only
  auto outAt   = [&](double in) { const double g = compGrDb(in); return in + g + gateGrDb(in, g); };

  // clip to the plot well so curves never spill over the rounded corners
  ctx.save();
  ctx.clip_to_rect(BLRect(px0, py0, plotSide, plotSide));

  // unity 1:1 diagonal (faint reference)
  ctx.set_stroke_width(1.0);
  ctx.stroke_line(BLLine(px0, py1, px1, py0), col(dynui::kCurveUnity));

  const int N = dynui::kPlotSamples;
  auto sampleDb = [&](int i) { return inMin + span * (double)i / (double)N; };

  // reduction wedge: area between unity diagonal and the COMPRESSION curve (gate
  // excluded so red = compression reduction only), red->transparent
  {
    BLPath wedge;
    wedge.move_to(dbToX(inMin), dbToY(inMin));
    wedge.line_to(dbToX(inMax), dbToY(inMax));        // along the diagonal
    for (int i = N; i >= 0; --i) {                    // back along the curve
      const double in = sampleDb(i);
      wedge.line_to(dbToX(in), dbToY(compOut(in)));
    }
    wedge.close();
    BLGradient g(BLLinearGradientValues(0, py0, 0, py1));
    g.add_stop(0.0, colA(dynui::kGrRed, 70));
    g.add_stop(1.0, colA(dynui::kGrRed, 0));
    ctx.fill_path(wedge, g);
  }

  // dotted threshold line (vertical), persists regardless of overlays
  {
    const double tx = dbToX(p.thresholdDb);
    ctx.set_stroke_width(1.0);
    for (double yy = py0; yy < py1; yy += 5.0)
      ctx.stroke_line(BLLine(tx, yy, tx, std::min(yy + 2.5, py1)),
                      colA(dynui::kInkMuted, 200));
  }

  // static transfer curve (neutral), full shape including the gate drop
  BLPath curve;
  for (int i = 0; i <= N; ++i) {
    const double in = sampleDb(i);
    const double cx = dbToX(in), cy = dbToY(outAt(in));
    if (i == 0) curve.move_to(cx, cy); else curve.line_to(cx, cy);
  }
  ctx.set_stroke_width(2.0);
  ctx.stroke_path(curve, col(dynui::kCurveStatic));

  // gate cliff: re-stroke the input subrange where the gate is active, in violet.
  // Onset is at input (gateThresh - makeup); depth ramps to the gate range and
  // clamps - matches engine min(|gateThr-postLevel|,|gateRange|) on the
  // post-comp+makeup level. Drawn ON the curve (outAt) so it stays continuous.
  if (p.showGate) {
    BLPath gate;
    bool started = false;
    for (int i = 0; i <= N; ++i) {
      const double in = sampleDb(i);
      if (gateGrDb(in, compGrDb(in)) < 0.0) {
        const double cx = dbToX(in), cy = dbToY(outAt(in));
        if (!started) { gate.move_to(cx, cy); started = true; } else gate.line_to(cx, cy);
      }
    }
    if (started) {
      ctx.set_stroke_width(2.0);
      ctx.stroke_path(gate, col(dynui::kGateViolet));
    }
  }

  // live segment up to the operating point (avgPeak), amber + glow
  const bool hasOp = p.avgPeakDb > inMin && p.avgPeakDb <= inMax;
  if (hasOp) {
    BLPath lit;
    bool started = false;
    for (int i = 0; i <= N; ++i) {
      const double in = sampleDb(i);
      if (in > p.avgPeakDb) break;
      const double cx = dbToX(in), cy = dbToY(outAt(in));
      if (!started) { lit.move_to(cx, cy); started = true; } else lit.line_to(cx, cy);
    }
    lit.line_to(dbToX(p.avgPeakDb), dbToY(outAt(p.avgPeakDb)));
    GlowStroke(ctx, lit, dynui::kAmberGlow);
    ctx.set_stroke_width(2.0);
    ctx.stroke_path(lit, col(dynui::kAmber));

    // operating-point dot + glow
    const double dx = dbToX(p.avgPeakDb), dy = dbToY(outAt(p.avgPeakDb));
    ctx.set_comp_op(BL_COMP_OP_PLUS);
    ctx.fill_circle(BLCircle(dx, dy, 7.0), colA(dynui::kAmberGlow, 60));
    ctx.fill_circle(BLCircle(dx, dy, 4.5), colA(dynui::kAmberGlow, 110));
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    ctx.fill_circle(BLCircle(dx, dy, 3.0), col(dynui::kAmber));
  }

  ctx.restore();   // remove plot clip

  // input-axis dB tick labels on a 12 dB grid within [inMin, inMax]
  if (gfx.fontsReady) {
    const BLFont& f = gfx.fTick;
    const double step = (double)dynui::kTickStepDb;
    for (double db = std::ceil(inMin / step) * step; db <= inMax + 0.001; db += step) {
      char buf[8];
      std::snprintf(buf, sizeof(buf), "%d", (int)std::lround(db));
      ctx.fill_utf8_text(BLPoint(dbToX(db) + 2.0, py1 + 11.0), f, buf,
                         SIZE_MAX, col(dynui::kInkMuted));
    }
  }
}

// GR meter to the right of the plot (anchored at px1): track + downward red fill
// + ticks + peak-hold marker + hero numeric readout. (must-dos #2/#3 refine the
// value + scale at Inc 2; this is the verbatim spike behaviour.)
static void DrawGrMeter(BLContext& ctx, const Gfx& gfx,
                        double px1, double py0, double plotSide,
                        const DynCurveParams& p)
{
  // Value = real engine average GR (p.avgGrDb, negative) -> magnitude: the truthful
  // applied/average GR (what auto-makeup compensated, what Apply does). Scale = the
  // PLOT's px-per-dB so the fill height equals the curve-to-diagonal gap (must-do
  // #2: shared scale, not a private 24 dB range).
  const double span = (p.inMaxDb - p.inMinDb) != 0 ? (p.inMaxDb - p.inMinDb) : 1.0;
  const double pxPerDb = plotSide / span;
  const double grMag = std::max(0.0, -p.avgGrDb);
  const double meterX = px1 + (double)dynui::kMeterGap;
  const double barW = (double)dynui::kMeterBarW;
  const double barTop = py0;
  const double barH = plotSide;

  ctx.fill_round_rect(BLRoundRect(meterX, barTop, barW, barH, 4.0),
                      col(dynui::kSurface2));
  const double fillH = std::clamp(grMag * pxPerDb, 0.0, barH);
  if (fillH > 0.5) {
    BLGradient g(BLLinearGradientValues(0, barTop, 0, barTop + barH));
    g.add_stop(0.0, col(dynui::kGrRed));
    g.add_stop(1.0, colA(dynui::kGrRed, 120));
    ctx.fill_round_rect(BLRoundRect(meterX, barTop, barW, fillH, 4.0), g);
    ctx.fill_rect(BLRect(meterX, barTop + fillH - 1.0, barW, 2.0),
                  col(dynui::kAmberGlow));   // peak-hold marker
  }
  const double ticks[] = { 0.0, 3.0, 6.0, 12.0, 24.0 };
  ctx.set_stroke_width(1.0);
  for (double t : ticks) {
    const double ty = barTop + std::clamp(t * pxPerDb, 0.0, barH);
    ctx.stroke_line(BLLine(meterX - 3.0, ty, meterX, ty), colA(dynui::kInkMuted, 200));
  }

  const double numX = meterX + barW + (double)dynui::kMeterNumGap;
  char num[16];
  std::snprintf(num, sizeof(num), "%.1f", grMag);
  if (gfx.fontsReady) {
    ctx.fill_utf8_text(BLPoint(numX, barTop + 13.0), gfx.fLabel, "GR", SIZE_MAX,
                       col(dynui::kInkSecondary));
    ctx.fill_utf8_text(BLPoint(numX, barTop + 40.0), gfx.fGrHero, num, SIZE_MAX,
                       col(dynui::kGrRed));
    const double numAdvance = TextWidth(gfx.fGrHero, num);
    ctx.fill_utf8_text(BLPoint(numX + numAdvance + 5.0, barTop + 40.0), gfx.fUnit, "dB",
                       SIZE_MAX, col(dynui::kInkMuted));
  }
}

// Ensure the platform surface + cached BLImage/fonts are ready at devW x devH.
bool UiCanvas::prepareSurface(HDC hdc, int devW, int devH)
{
  if (!ensure(hdc, devW, devH)) return false;
  if (!m_gfx) m_gfx = std::make_unique<Gfx>();
  m_gfx->ensureFonts();
  return m_gfx->ensureImage(devW, devH);
}

// Copy the rendered BLImage scanlines into the platform framebuffer (forcing
// opaque alpha), then scaled-blit to the destination DC. dpr==1 -> devW==w -> 1:1
// (Windows/Linux unchanged); dpr>1 onto the real Retina window DC -> SWELL maps
// the 2x source onto the 2x backing => crisp HiDPI. (Plain BitBlt(devW,devH) would
// place device-sized px at a logical position = 2x-too-large; the BitBlt-1:1 trap
// documented in .harness/design_phase2_architecture.md.)
void UiCanvas::presentSurface(HDC hdc, int x, int y, int w, int h, int devW, int devH)
{
  unsigned int* fbuf = nullptr;
#ifdef _WIN32
  BITMAP bm;
  if (GetObject(m_memBmp, sizeof(bm), &bm)) fbuf = (unsigned int*)bm.bmBits;
#else
  fbuf = (unsigned int*)SWELL_GetCtxFrameBuffer(m_memDC);
#endif
  if (!fbuf) return;

  BLImageData dat;
  if (m_gfx->img.get_data(&dat) != BL_SUCCESS) return;
  if (!dat.pixel_data) return;
  if (dat.stride <= 0 || (dat.stride % 4) != 0 || dat.stride < (intptr_t)devW * 4) return;
  const uint8_t* srcBase = (const uint8_t*)dat.pixel_data;
  for (int row = 0; row < devH; ++row) {
    const unsigned int* s = (const unsigned int*)(srcBase + (intptr_t)row * dat.stride);
    unsigned int* d = fbuf + (size_t)row * (size_t)devW;
    for (int col2 = 0; col2 < devW; ++col2)
      d[col2] = s[col2] | 0xFF000000u;
  }
  StretchBlt(hdc, x, y, w, h, m_memDC, 0, 0, devW, devH, SRCCOPY);
}

// --- the hero render (spike) -----------------------------------------------

void UiCanvas::RenderTransferCurve(HDC hdc, int x, int y, int w, int h, double dpr,
                                   const DynCurveParams& p)
{
  if (!hdc || w < 8 || h < 8) return;
  if (dpr < 1.0) dpr = 1.0;
  const int devW = (int)std::lround(w * dpr);
  const int devH = (int)std::lround(h * dpr);
  if (!prepareSurface(hdc, devW, devH)) return;
  {
    BLContext ctx;
    if (ctx.begin(m_gfx->img) != BL_SUCCESS) return;
    ctx.clear_all();                // cached image persists prior pixels - start clean
    ctx.scale(dpr);                 // draw everything in LOGICAL coordinates
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);

    const double W = w, H = h;

    // --- panel slab: vertical gradient + rounded body + top hairline highlight
    {
      BLGradient bg(BLLinearGradientValues(0, 0, 0, H));
      bg.add_stop(0.0, col(dynui::kSurface1));
      bg.add_stop(1.0, col(dynui::kSurface0));
      ctx.fill_round_rect(BLRoundRect(0, 0, W, H, dynui::kRadiusPanel), bg);
      ctx.set_stroke_width(1.0);
      ctx.stroke_round_rect(BLRoundRect(0.5, 0.5, W - 1, H - 1, dynui::kRadiusPanel),
                            col(dynui::kHairline));
    }

    // --- plot well geometry (square, inset); helpers draw the content ---
    const double pad = dynui::kPanelPad;
    const double plotSide = std::min(W - 2 * pad, H - 2 * pad);
    const double px0 = pad;
    const double py0 = (H - plotSide) * 0.5;
    const double px1 = px0 + plotSide;

    DrawTransferPlot(ctx, *m_gfx, px0, py0, plotSide, p);
    DrawGrMeter(ctx, *m_gfx, px1, py0, plotSide, p);

    if (ctx.end() != BL_SUCCESS) return;
  }
  presentSurface(hdc, x, y, w, h, devW, devH);
}
