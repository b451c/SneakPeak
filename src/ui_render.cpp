// Transfer-curve plot inside the square well at (px0,py0,plotSide): well bg,
// unity diagonal, reduction wedge, dotted threshold, gate cliff, static curve,
// amber operating-point segment + glow dot, and input-axis dB tick labels.
// Drawn in logical coordinates; mirrors dynamics_engine ComputeCompression math.
static void DrawTransferPlot(BLContext& ctx, const Gfx& gfx,
                             double px0, double py0, double plotSide,
                             const DynCurveParams& p, int activeTab = 2)
{
  const double px1 = px0 + plotSide;
  const double py1 = py0 + plotSide;
  // Per-tab emphasis (design spec sec 6): the Compressor tab dims the gate, the Gate tab
  // dims the compressor, the View tab (2) lights both. The off-focus processor's coloured
  // elements (red comp wedge + comp threshold / violet gate cliff) drop to ~30% alpha so
  // the focused processor stands out; the neutral curve + operating point stay full.
  const double compA = (activeTab == 1) ? 0.30 : 1.0;   // dim compressor on the Gate tab
  const double gateA = (activeTab == 0) ? 0.30 : 1.0;   // dim gate on the Compressor tab
  ctx.fill_round_rect(BLRoundRect(px0, py0, plotSide, plotSide, dynui::kRadiusCtrl),
                      col(dynui::kSurface1));

  const double inMin = p.inMinDb, inMax = p.inMaxDb;
  const double span = (inMax - inMin) != 0 ? (inMax - inMin) : 1.0;
  auto dbToX = [&](double db) { return px0 + (db - inMin) / span * plotSide; };
  auto dbToY = [&](double db) { return py1 - (db - inMin) / span * plotSide; };

  // Curve math mirrors dynamics_engine ComputeCompression() steady state, INCLUDING
  // makeup: the plotted curve is the true output y = x + GR + makeup + gate (engine
  // :306), so the amber line rises with the Makeup knob and never lies about what
  // Apply does (the anti-slop "curve == Apply" rule). Makeup is a constant vertical
  // shift; the reduction wedge measures from the makeup baseline (x+makeup), so red
  // still = pure gain reduction. The unity diagonal stays as the 1:1 reference -
  // the gap between it and the curve at low input is the visible makeup boost.
  auto compGrDb = [&](double in) {
    return CompressionGrDb(in, p);   // <= 0 in Down; capped boost >= 0 in Up
  };
  auto gateGrDb = [&](double in, double compGr) -> double {
    if (!p.showGate) return 0.0;
    const double detect = (p.mode != 0) ? in : in + compGr;                  // Up/Both: raw; Down: post-comp, PRE-makeup
    if (detect >= p.gateThreshDb) return 0.0;
    const double gateRangeNeg = -std::fabs(p.gateRangeDb);                   // engine range is negative
    const double gateSlope = std::max(1.0, p.gateRatio) - 1.0;               // closed-state slope Rg-1
    return std::max(gateSlope * (detect - p.gateThreshDb), gateRangeNeg);    // ramp 0 -> range, clamped
  };
  auto compOut = [&](double in) { return in + compGrDb(in) + p.makeupDb; }; // wedge bottom: comp + makeup
  auto outAt   = [&](double in) { return CurveOutDb(in, p); };             // full output (shared w/ handles)

  // clip to the plot well so curves never spill over the rounded corners
  ctx.save();
  ctx.clip_to_rect(BLRect(px0, py0, plotSide, plotSide));

  // unity 1:1 diagonal (faint reference)
  ctx.set_stroke_width(1.0);
  ctx.stroke_line(BLLine(px0, py1, px1, py0), col(dynui::kCurveUnity));

  // Hysteresis band (v2.3.0 gate extension): with H < 0 the gate OPENS at tOpen
  // but stays open down to tClose = tOpen + H, so the instantaneous behaviour in
  // between is state-dependent - the faint violet band keeps the static plot
  // honest about that. The band lives in the DETECT domain; detect(x) = x +
  // compGR(x) is strictly increasing (comp slope > -1), so it inverts to an
  // input range by bisection. Dims with the gate on the Compressor tab.
  if (p.showGate && p.gateHystDb < 0.0) {
    // Detection domain mirrors the engine: Up gates on the RAW input (x axis).
    auto detectAt = [&](double in) { return (p.mode != 0) ? in : in + compGrDb(in); };
    auto invDetect = [&](double target) {
      double lo = inMin, hi = inMax;
      if (detectAt(lo) >= target) return lo;
      if (detectAt(hi) <= target) return hi;
      for (int it = 0; it < 24; ++it) {
        const double mid = 0.5 * (lo + hi);
        if (detectAt(mid) < target) lo = mid; else hi = mid;
      }
      return 0.5 * (lo + hi);
    };
    const double xC = dbToX(invDetect(p.gateThreshDb + p.gateHystDb));
    const double xO = dbToX(invDetect(p.gateThreshDb));
    if (xO - xC > 0.5)
      ctx.fill_rect(BLRect(xC, py0, xO - xC, plotSide),
                    colA(dynui::kGateViolet, (int)(30.0 * gateA)));
  }

  const int N = dynui::kPlotSamples;
  auto sampleDb = [&](int i) { return inMin + span * (double)i / (double)N; };

  // reduction wedge: area between the makeup baseline (x+makeup) and the COMPRESSION
  // curve (x+compGR+makeup; gate excluded so red = compression reduction only). The
  // gap equals -compGR regardless of makeup, so red stays = pure reduction.
  // Per-column vertical gradient from the makeup baseline (x+makeup, opaque) DOWN to the
  // compression curve (x+compGR+makeup, transparent) so the red fades to NOTHING exactly
  // at the curve - a single full-height gradient would be cut with a hard edge wherever
  // the curve crosses it mid-plot. Each thin strip's gradient is local to its own
  // baseline->curve span; adjacent strips share an edge (no overlap/seam). Columns with
  // no visible reduction are skipped. gap = -compGR = compression reduction (gate excluded).
  {
    const int topAlpha = (int)std::lround(70.0 * compA);
    for (int i = 0; i < N; ++i) {
      const double inA = sampleDb(i), inB = sampleDb(i + 1);
      const double xA = dbToX(inA), xB = dbToX(inB);
      const double baseA = dbToY(inA + p.makeupDb), baseB = dbToY(inB + p.makeupDb);
      const double curvA = dbToY(compOut(inA)), curvB = dbToY(compOut(inB));
      if (curvA - baseA < 0.5 && curvB - baseB < 0.5) continue;   // curve sits on the baseline -> no reduction
      BLPath strip;
      strip.move_to(xA, baseA); strip.line_to(xB, baseB);
      strip.line_to(xB, curvB); strip.line_to(xA, curvA); strip.close();
      BLGradient g(BLLinearGradientValues(0, 0.5 * (baseA + baseB), 0, 0.5 * (curvA + curvB)));
      g.add_stop(0.0, colA(dynui::kGrRed, topAlpha));   // opaque at the makeup baseline
      g.add_stop(1.0, colA(dynui::kGrRed, 0));          // transparent at the curve (soft edge)
      ctx.fill_path(strip, g);
    }
  }

  // dotted threshold line (vertical), persists regardless of overlays
  {
    const double tx = dbToX(p.thresholdDb);
    ctx.set_stroke_width(1.0);
    for (double yy = py0; yy < py1; yy += 5.0)
      ctx.stroke_line(BLLine(tx, yy, tx, std::min(yy + 2.5, py1)),
                      colA(dynui::kInkMuted, (int)std::lround(200.0 * compA)));
  }

  // static transfer curve (neutral), full shape including the gate drop
  BLPath curve;
  for (int i = 0; i <= N; ++i) {
    const double in = sampleDb(i);
    const double cx = dbToX(in), cy = dbToY(outAt(in));
    if (i == 0) curve.move_to(cx, cy); else curve.line_to(cx, cy);
  }
  ctx.set_stroke_width(2.0);
  ctx.stroke_path(curve, col(dynui::kCurveStatic));

  // gate cliff: re-stroke the input subrange where the gate is active, in violet.
  // Onset is at input where the post-comp (pre-makeup) level crosses gateThresh - i.e.
  // input == gateThresh while below the comp knee - so the gate node at input=gateThresh
  // sits on the cliff regardless of makeup (makeup only lifts the whole curve). Depth
  // ramps to the gate range + clamps. Drawn ON the curve (outAt) so it stays continuous.
  if (p.showGate) {
    BLPath gate;
    bool started = false;
    for (int i = 0; i <= N; ++i) {
      const double in = sampleDb(i);
      if (gateGrDb(in, compGrDb(in)) < 0.0) {
        const double cx = dbToX(in), cy = dbToY(outAt(in));
        if (!started) { gate.move_to(cx, cy); started = true; } else gate.line_to(cx, cy);
      }
    }
    if (started) {
      ctx.set_stroke_width(2.0);
      ctx.stroke_path(gate, colA(dynui::kGateViolet, (int)std::lround(255.0 * gateA)));
    }
  }

  // live segment up to the operating point (avgPeak), amber + glow
  const bool hasOp = p.avgPeakDb > inMin && p.avgPeakDb <= inMax;
  if (hasOp) {
    BLPath lit;
    bool started = false;
    for (int i = 0; i <= N; ++i) {
      const double in = sampleDb(i);
      if (in > p.avgPeakDb) break;
      const double cx = dbToX(in), cy = dbToY(outAt(in));
      if (!started) { lit.move_to(cx, cy); started = true; } else lit.line_to(cx, cy);
    }
    lit.line_to(dbToX(p.avgPeakDb), dbToY(outAt(p.avgPeakDb)));
    GlowStroke(ctx, lit, dynui::kAmberGlow);
    ctx.set_stroke_width(2.0);
    ctx.stroke_path(lit, col(dynui::kAmber));

    // operating-point dot + glow
    const double dx = dbToX(p.avgPeakDb), dy = dbToY(outAt(p.avgPeakDb));
    ctx.set_comp_op(BL_COMP_OP_PLUS);
    ctx.fill_circle(BLCircle(dx, dy, 7.0), colA(dynui::kAmberGlow, 60));
    ctx.fill_circle(BLCircle(dx, dy, 4.5), colA(dynui::kAmberGlow, 110));
    ctx.set_comp_op(BL_COMP_OP_SRC_OVER);
    ctx.fill_circle(BLCircle(dx, dy, 3.0), col(dynui::kAmber));
  }

  ctx.restore();   // remove plot clip

  // input-axis dB tick labels on a 12 dB grid within [inMin, inMax]
  if (gfx.fontsReady) {
    const BLFont& f = gfx.fTick;
    const double step = (double)dynui::kTickStepDb;
    for (double db = std::ceil(inMin / step) * step; db <= inMax + 0.001; db += step) {
      char buf[8];
      std::snprintf(buf, sizeof(buf), "%d", (int)std::lround(db));
      ctx.fill_utf8_text(BLPoint(dbToX(db) + 2.0, py1 + 11.0), f, buf,
                         SIZE_MAX, col(dynui::kInkMuted));
    }
  }
}

// GR meter to the right of the plot (anchored at px1): track + downward red fill
// + ticks + peak-hold marker + hero numeric readout. (must-dos #2/#3 refine the
// value + scale at Inc 2; this is the verbatim spike behaviour.)
static void DrawGrMeter(BLContext& ctx, const Gfx& gfx,
                        double px1, double py0, double plotSide,
                        const DynCurveParams& p, bool withNumber = true)
{
  // Value = real engine average GR (p.avgGrDb, negative) -> magnitude: the truthful
  // applied/average GR (what auto-makeup compensated, what Apply does). Scale = the
  // PLOT's px-per-dB so the fill height equals the curve-to-diagonal gap (must-do
  // #2: shared scale, not a private 24 dB range).
  const double span = (p.inMaxDb - p.inMinDb) != 0 ? (p.inMaxDb - p.inMinDb) : 1.0;
  const double pxPerDb = plotSide / span;
  // Up mode: avgGrDb goes POSITIVE (boost) - same bar geometry, amber fill
  // (user-locked 2026-07-02); Down keeps the red reduction fill.
  const bool boost = p.avgGrDb > 0.0;
  const double grMag = boost ? p.avgGrDb : std::max(0.0, -p.avgGrDb);
  const uint32_t fillCol = boost ? dynui::kAmber : dynui::kGrRed;
  const double meterX = px1 + (double)dynui::kMeterGap;
  const double barW = (double)dynui::kMeterBarW;
  const double barTop = py0;
  const double barH = plotSide;

  ctx.fill_round_rect(BLRoundRect(meterX, barTop, barW, barH, 4.0),
                      col(dynui::kSurface2));
  const double fillH = std::clamp(grMag * pxPerDb, 0.0, barH);
  if (fillH > 0.5) {
    BLGradient g(BLLinearGradientValues(0, barTop, 0, barTop + barH));
    g.add_stop(0.0, col(fillCol));
    g.add_stop(1.0, colA(fillCol, 120));
    ctx.fill_round_rect(BLRoundRect(meterX, barTop, barW, fillH, 4.0), g);
    ctx.fill_rect(BLRect(meterX, barTop + fillH - 1.0, barW, 2.0),
                  col(dynui::kAmberGlow));   // peak-hold marker
  }
  const double ticks[] = { 0.0, 3.0, 6.0, 12.0, 24.0 };
  ctx.set_stroke_width(1.0);
  for (double t : ticks) {
    const double ty = barTop + std::clamp(t * pxPerDb, 0.0, barH);
    ctx.stroke_line(BLLine(meterX - 3.0, ty, meterX, ty), colA(dynui::kInkMuted, 200));
  }

  // Numeric hero column (spike layout). The premium panel passes withNumber=false
  // and renders the GR hero number in the header instead (compact grid needs the
  // body width for the knob grid).
  if (withNumber && gfx.fontsReady) {
    const double numX = meterX + barW + (double)dynui::kMeterNumGap;
    char num[16];
    std::snprintf(num, sizeof(num), "%.1f", grMag);
    ctx.fill_utf8_text(BLPoint(numX, barTop + 13.0), gfx.fLabel, "GR", SIZE_MAX,
                       col(dynui::kInkSecondary));
    ctx.fill_utf8_text(BLPoint(numX, barTop + 40.0), gfx.fGrHero, num, SIZE_MAX,
                       col(dynui::kGrRed));
    const double numAdvance = TextWidth(gfx.fGrHero, num);
    ctx.fill_utf8_text(BLPoint(numX + numAdvance + 5.0, barTop + 40.0), gfx.fUnit, "dB",
                       SIZE_MAX, col(dynui::kInkMuted));
  }
}

